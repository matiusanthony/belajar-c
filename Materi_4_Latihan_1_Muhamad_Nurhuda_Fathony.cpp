#include <iostream>

using namespace std;

int main()
{

    int i=1;
    for(i=1;i<=5;i++)
    {

        cout<<"Perulangan dengan for nilai i = "<<i<<endl;
    }
    i=1;
    while (i>0,i<6)
    {
        cout<<"Perulangan dengan while nilai i = "<<i<<endl;
        i=i+1;
    }
    i=1;
    do {
        cout<<"Perulangan dengan do-while nilai i = "<<i<<endl;
        i=i+1;

    }while (i>0,i<6);

    return 0;
}
